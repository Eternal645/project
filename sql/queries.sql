-- ────────────────────────────────────────────────────────────
-- 1. ПРОСТЫЕ ЗАПРОСЫ
-- ────────────────────────────────────────────────────────────

-- 1.1  Все товары в наличии (остаток > 0)
SELECT id, наименование, производитель, цена, остаток
FROM Товары
WHERE остаток > 0
ORDER BY наименование;

-- 1.2  Список клиентов (по алфавиту)
SELECT id, фио, телефон, email
FROM Клиенты
ORDER BY фио;

-- 1.3  Сотрудники
SELECT id, фио, должность, телефон, зарплата
FROM Сотрудники
ORDER BY фио;

-- 1.4  Все поставщики
SELECT id, название, контактное_лицо, телефон, email
FROM Поставщики
ORDER BY название;

-- 1.5  Заказы со статусом «новый»
SELECT id, id_клиента, дата_заказа, итого, способ_оплаты
FROM Заказы
WHERE статус = 'новый'
ORDER BY дата_заказа DESC;

-- 1.6  Доставки в пути
SELECT id, id_заказа, адрес_доставки, дата_доставки, курьер
FROM Доставки
WHERE статус = 'в пути'
ORDER BY дата_доставки;


-- ────────────────────────────────────────────────────────────
-- 2. ВЫЧИСЛЯЕМЫЕ ЗАПРОСЫ (COUNT / SUM / AVG)
-- ────────────────────────────────────────────────────────────

-- 2.1  Количество товаров по категориям
SELECT к.название AS категория, COUNT(т.id) AS кол_во_товаров
FROM Категории к
LEFT JOIN Товары т ON т.id_категории = к.id
GROUP BY к.id
ORDER BY кол_во_товаров DESC;

-- 2.2  Общая выручка по выполненным заказам
SELECT COUNT(*) AS заказов, printf('%.2f', SUM(итого)) AS выручка
FROM Заказы
WHERE статус = 'выполнен';

-- 2.3  Средняя цена товара по категориям
SELECT к.название AS категория,
       printf('%.2f', AVG(т.цена)) AS средняя_цена,
       printf('%.2f', MIN(т.цена)) AS мин_цена,
       printf('%.2f', MAX(т.цена)) AS макс_цена
FROM Товары т
JOIN Категории к ON т.id_категории = к.id
GROUP BY к.id
ORDER BY AVG(т.цена) DESC;

-- 2.4  Статистика заказов по статусу
SELECT статус,
       COUNT(*) AS кол_во,
       printf('%.2f', SUM(итого)) AS сумма
FROM Заказы
GROUP BY статус;

-- 2.5  Суммарная стоимость склада по категориям
SELECT к.название AS категория,
       SUM(т.остаток) AS единиц_на_складе,
       printf('%.2f', SUM(т.остаток * т.цена)) AS стоимость
FROM Товары т
JOIN Категории к ON т.id_категории = к.id
GROUP BY к.id
ORDER BY SUM(т.остаток * т.цена) DESC;


-- ────────────────────────────────────────────────────────────
-- 3. ЗАПРОСЫ С ПАРАМЕТРАМИ  (параметр обозначен символом ?)
-- ────────────────────────────────────────────────────────────

-- 3.1  Товары по категории  (параметр: название категории)
SELECT т.id, т.наименование, т.производитель,
       printf('%.2f', т.цена) AS цена, т.остаток
FROM Товары т
JOIN Категории к ON т.id_категории = к.id
WHERE к.название = ?
ORDER BY т.наименование;

-- 3.2  Заказы в диапазоне дат  (параметры: дата_с, дата_по)
SELECT з.id, к.фио AS клиент, з.дата_заказа,
       з.статус, printf('%.2f', з.итого) AS итого, з.способ_оплаты
FROM Заказы з
JOIN Клиенты к ON з.id_клиента = к.id
WHERE з.дата_заказа BETWEEN ? AND ?
ORDER BY з.дата_заказа DESC;

-- 3.3  Товары в ценовом диапазоне  (параметры: цена_мин, цена_макс)
SELECT наименование, производитель,
       printf('%.2f', цена) AS цена, остаток
FROM Товары
WHERE цена BETWEEN ? AND ?
ORDER BY цена;

-- 3.4  Поиск клиента по телефону  (параметр: телефон)
SELECT id, фио, телефон, email, адрес, дата_регистрации
FROM Клиенты
WHERE телефон = ?;

-- 3.5  Заказы конкретного клиента  (параметр: id клиента)
SELECT з.id, з.дата_заказа, з.статус,
       printf('%.2f', з.итого) AS итого, з.способ_оплаты
FROM Заказы з
WHERE з.id_клиента = ?
ORDER BY з.дата_заказа DESC;


-- ────────────────────────────────────────────────────────────
-- 4. ЗАПРОСЫ С JOIN
-- ────────────────────────────────────────────────────────────

-- 4.1  Полный список заказов с именем клиента и сотрудника
SELECT з.id,
       к.фио  AS клиент,
       COALESCE(с.фио, '(не указан)') AS менеджер,
       з.дата_заказа, з.статус,
       printf('%.2f', з.итого) AS итого,
       з.способ_оплаты
FROM Заказы з
JOIN Клиенты к  ON з.id_клиента    = к.id
LEFT JOIN Сотрудники с ON з.id_сотрудника = с.id
ORDER BY з.дата_заказа DESC;

-- 4.2  Состав заказов (строки + товары)
SELECT сз.id_заказа, т.наименование,
       сз.количество,
       printf('%.2f', сз.цена_единицы) AS цена_ед,
       printf('%.2f', сз.количество * сз.цена_единицы) AS сумма_строки
FROM СтрокиЗаказа сз
JOIN Товары т ON сз.id_товара = т.id
ORDER BY сз.id_заказа, т.наименование;

-- 4.3  Поставки с наименованием поставщика
SELECT п.id, пост.название AS поставщик,
       п.дата_поставки, printf('%.2f', п.сумма) AS сумма
FROM Поставки п
JOIN Поставщики пост ON п.id_поставщика = пост.id
ORDER BY п.дата_поставки DESC;

-- 4.4  Доставки с данными заказа и клиента
SELECT д.id, к.фио AS клиент,
       д.адрес_доставки, д.дата_доставки,
       д.курьер, д.статус
FROM Доставки д
JOIN Заказы з ON д.id_заказа = з.id
JOIN Клиенты к ON з.id_клиента = к.id
ORDER BY д.дата_доставки DESC;

-- 4.5  ТОП-10 товаров по выручке
SELECT т.наименование,
       SUM(сз.количество) AS продано_шт,
       printf('%.2f', SUM(сз.количество * сз.цена_единицы)) AS выручка
FROM СтрокиЗаказа сз
JOIN Товары т ON сз.id_товара = т.id
JOIN Заказы з ON сз.id_заказа = з.id
WHERE з.статус != 'отменён'
GROUP BY т.id
ORDER BY SUM(сз.количество * сз.цена_единицы) DESC
LIMIT 10;


-- ────────────────────────────────────────────────────────────
-- 5. АНАЛИТИЧЕСКИЕ ОТЧЁТЫ
-- ────────────────────────────────────────────────────────────

-- 5.1  Выручка по месяцам
SELECT substr(дата_заказа, 1, 7) AS период,
       COUNT(*) AS заказов,
       printf('%.2f', SUM(итого)) AS выручка
FROM Заказы
WHERE статус = 'выполнен'
GROUP BY substr(дата_заказа, 1, 7)
ORDER BY период;

-- 5.2  VIP-клиенты (сумма заказов > 50 000 руб.)
SELECT к.фио, к.телефон,
       COUNT(з.id) AS заказов,
       printf('%.2f', SUM(з.итого)) AS сумма
FROM Клиенты к
JOIN Заказы з ON к.id = з.id_клиента
WHERE з.статус != 'отменён'
GROUP BY к.id
HAVING SUM(з.итого) > 50000
ORDER BY SUM(з.итого) DESC;

-- 5.3  Менеджеры по продажам
SELECT COALESCE(с.фио, '(не указан)') AS сотрудник,
       с.должность,
       COUNT(з.id) AS заказов,
       printf('%.2f', SUM(з.итого)) AS выручка
FROM Заказы з
LEFT JOIN Сотрудники с ON з.id_сотрудника = с.id
WHERE з.статус != 'отменён'
GROUP BY з.id_сотрудника
ORDER BY SUM(з.итого) DESC;

-- 5.4  Товары с критически малым остатком (< 4 шт.)
SELECT к.название AS категория, т.наименование,
       т.остаток, printf('%.2f', т.цена) AS цена
FROM Товары т
JOIN Категории к ON т.id_категории = к.id
WHERE т.остаток < 4
ORDER BY т.остаток;

-- 5.5  Ценовые сегменты товаров
SELECT
    CASE
        WHEN цена < 5000  THEN 'до 5 000'
        WHEN цена < 20000 THEN '5 000 – 20 000'
        ELSE 'свыше 20 000'
    END AS сегмент,
    COUNT(*) AS товаров,
    printf('%.2f', AVG(цена)) AS средняя_цена
FROM Товары
GROUP BY сегмент
ORDER BY AVG(цена);
