-- [report_выручка_по_месяцам]
SELECT substr(з.дата_заказа,1,7)              AS период,
       COUNT(*)                                AS заказов,
       printf('%.2f', SUM(з.итого))            AS выручка
FROM Заказы з
JOIN СтатусыЗаказа сз ON з.id_статуса = сз.id
WHERE сз.название = 'выполнен'
GROUP BY substr(з.дата_заказа,1,7)
ORDER BY период;

-- [report_топ10_товаров]
WITH итого AS (
    SELECT SUM(сз2.количество * сз2.цена_единицы) AS total
    FROM СтрокиЗаказа сз2
    JOIN Заказы з2 ON сз2.id_заказа = з2.id
    JOIN СтатусыЗаказа ст2 ON з2.id_статуса = ст2.id
    WHERE ст2.название != 'отменён'
)
SELECT т.наименование,
       к.название                                           AS категория,
       SUM(сз.количество)                                   AS продано_шт,
       printf('%.2f', SUM(сз.количество * сз.цена_единицы)) AS выручка,
       printf('%.1f%%',
           100.0 * SUM(сз.количество * сз.цена_единицы)
           / NULLIF((SELECT total FROM итого), 0))          AS доля
FROM СтрокиЗаказа сз
JOIN Товары т    ON сз.id_товара  = т.id
JOIN Категории к ON т.id_категории = к.id
JOIN Заказы з    ON сз.id_заказа   = з.id
JOIN СтатусыЗаказа ст ON з.id_статуса = ст.id
WHERE ст.название != 'отменён'
GROUP BY т.id
ORDER BY продано_шт DESC
LIMIT 10;

-- [report_местонахождение_товаров]
SELECT т.наименование,
       к.название                           AS категория,
       COALESCE(м.название, '—')            AS место_хранения,
       COALESCE(м.описание, '')             AS описание_места,
       т.остаток                            AS шт
FROM Товары т
JOIN Категории к ON т.id_категории = к.id
LEFT JOIN МестаХранения м ON т.id_места_хранения = м.id
ORDER BY м.название, к.название, т.наименование;

-- [report_vip_клиенты]
SELECT кл.фио, кл.телефон,
       COUNT(з.id)                          AS заказов,
       printf('%.2f', SUM(з.итого))         AS сумма
FROM Клиенты кл
JOIN Заказы з   ON кл.id = з.id_клиента
JOIN СтатусыЗаказа ст ON з.id_статуса = ст.id
WHERE ст.название != 'отменён'
GROUP BY кл.id
HAVING SUM(з.итого) > 50000
ORDER BY SUM(з.итого) DESC;

-- [report_остатки_на_складе]
SELECT к.название                               AS категория,
       т.наименование,
       COALESCE(м.название, '—')               AS место,
       т.остаток                               AS шт,
       printf('%.2f', т.цена)                  AS цена,
       printf('%.2f', т.остаток * т.цена)      AS стоимость_запаса
FROM Товары т
JOIN Категории к ON т.id_категории = к.id
LEFT JOIN МестаХранения м ON т.id_места_хранения = м.id
ORDER BY к.название, т.наименование;

-- [report_малый_остаток]
SELECT к.название       AS категория,
       т.наименование,
       COALESCE(м.название, '—') AS место,
       т.остаток,
       printf('%.2f', т.цена) AS цена
FROM Товары т
JOIN Категории к ON т.id_категории = к.id
LEFT JOIN МестаХранения м ON т.id_места_хранения = м.id
WHERE т.остаток < 4
ORDER BY т.остаток;

-- [report_заказы_по_сотрудникам]
SELECT COALESCE(с.фио, '(не указан)')   AS сотрудник,
       с.должность,
       COUNT(з.id)                       AS заказов,
       printf('%.2f', SUM(з.итого))      AS выручка
FROM Заказы з
LEFT JOIN Сотрудники с ON з.id_сотрудника = с.id
JOIN СтатусыЗаказа ст  ON з.id_статуса    = ст.id
WHERE ст.название != 'отменён'
GROUP BY з.id_сотрудника
ORDER BY SUM(з.итого) DESC;

-- [report_статусы_доставок]
SELECT сд.название      AS статус,
       COUNT(*)         AS кол_во,
       GROUP_CONCAT(д.id_заказа, ', ') AS номера_заказов
FROM Доставки д
JOIN СтатусыДоставки сд ON д.id_статуса_доставки = сд.id
GROUP BY сд.id
ORDER BY сд.название;

-- [report_поставки_по_поставщикам]
SELECT пост.название,
       COUNT(п.id)                  AS поставок,
       printf('%.2f', SUM(п.сумма)) AS итого
FROM Поставки п
JOIN Поставщики пост ON п.id_поставщика = пост.id
GROUP BY пост.id
ORDER BY SUM(п.сумма) DESC;


