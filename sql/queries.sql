       SUM(з.итого)                         AS доход
FROM Заказы з
JOIN СтатусыЗаказа ст ON з.id_статуса = ст.id
WHERE ст.название = 'выполнен'
GROUP BY strftime('%Y-W%W', з.дата_заказа);

-- [profit_расход_неделя]
