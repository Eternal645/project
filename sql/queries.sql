-- ТАБЛИЦЫ: запросы для просмотра (TABLE_META / _load_combo
-- [table_категории]
SELECT id, название, описание FROM Категории ORDER BY название;

-- [table_места_хранения]
SELECT id, название, описание FROM МестаХранения ORDER BY название;

-- [table_поставщики]
SELECT id, название, контактное_лицо, телефон, email FROM Поставщики ORDER BY название;

-- [table_товары]
SELECT т.id, к.название AS категория, т.наименование, т.производитель,
       т.цена, т.остаток,
       COALESCE(м.название, '—') AS место,
       т.описание
FROM Товары т
JOIN Категории к ON т.id_категории = к.id
LEFT JOIN МестаХранения м ON т.id_места_хранения = м.id
ORDER BY к.название, т.наименование;

-- [table_клиенты]
SELECT id, фио, телефон, email, адрес, дата_регистрации FROM Клиенты ORDER BY фио;

-- [table_сотрудники]
SELECT id, фио, должность, телефон, зарплата FROM Сотрудники ORDER BY фио;

-- [table_заказы]
SELECT з.id, кл.фио AS клиент, с.фио AS сотрудник,
       з.дата_заказа,
       ст.название  AS статус,
       з.итого,
       оп.название  AS оплата
FROM Заказы з
JOIN Клиенты кл        ON з.id_клиента        = кл.id
LEFT JOIN Сотрудники с  ON з.id_сотрудника     = с.id
JOIN СтатусыЗаказа ст  ON з.id_статуса         = ст.id
JOIN СпособыОплаты оп  ON з.id_способа_оплаты  = оп.id
ORDER BY з.дата_заказа DESC;

-- [table_строки_заказа]
SELECT сз.id, з.id AS заказ, т.наименование AS товар,
       сз.количество, сз.цена_единицы,
       (сз.количество * сз.цена_единицы) AS сумма
FROM СтрокиЗаказа сз
JOIN Заказы з ON сз.id_заказа = з.id
JOIN Товары т  ON сз.id_товара = т.id
ORDER BY з.id, сз.id;

-- [table_поставки]
SELECT п.id, пост.название AS поставщик, п.дата_поставки, п.сумма
FROM Поставки п
JOIN Поставщики пост ON п.id_поставщика = пост.id
ORDER BY п.дата_поставки DESC;

-- [table_доставки]
SELECT д.id, д.id_заказа, кл.фио AS клиент,
       д.адрес_доставки, д.дата_доставки, д.курьер,
       сд.название AS статус
FROM Доставки д
JOIN Заказы з   ON д.id_заказа  = з.id
JOIN Клиенты кл ON з.id_клиента = кл.id
JOIN СтатусыДоставки сд ON д.id_статуса_доставки = сд.id
ORDER BY д.дата_доставки DESC;
